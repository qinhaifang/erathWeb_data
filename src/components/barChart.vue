<template>
  <div
    :id="barData.id"
    ref="myChart"
    :style="{ width: '100%', height: barData.height }"
  ></div>
</template>
<script>
import echarts from "echarts";
export default {
  props: ["barData"],
  data() {
    return {
      barChart: null,
    };
  },
  created() {
    console.log(55555,this.barData)
  },
  watch:{
    barData:function(newValue,oldValue){
      console.log('new',newValue,oldValue)
      this.drawline();
    },
    deep:true
  },
  mounted() {
    this.initChart()
    // vm.$nextTick(()=>{
    //   this.drawline();
    // })
    
  },
  methods: {
    setOptions({dataX,dataY} = {}) {
      var salvProMax =[];//背景按最大值
      for (let i = 0; i < dataX.length; i++) {
          salvProMax.push(dataX[0])
      }
      this.barChart.setOption({
        // backgroundColor: "#003366",
        grid: {
          left: "6%",
          right: "4%",
          bottom: "2%",
          top: "0%",
          containLabel: true,
        },
        tooltip: {
          trigger: "axis",
          axisPointer: {
            type: "none",
          },
          formatter: function (params) {
            return params[0].name + " : " + params[0].value + '万元';
          },
        },
        xAxis: {
          show: false,
          type: "value",
        },
        yAxis: [
          {
            type: "category",
            inverse: true,
            axisLabel: {
              show: true,
              textStyle: {
                color: "#fff",
              },
            },
            splitLine: {
              show: false,
            },
            axisTick: {
              show: false,
            },
            axisLine: {
              show: false,
            },
            // max:9,
            data: dataY,
          },
          {
            type: "category",
            inverse: true,
            axisTick: "none",
            axisLine: "none",
            // max:9,
            show: true,
            axisLabel: {
              textStyle: {
                color: "#ffffff",
                fontSize: "12",
              },
            },
            data: dataX,
          },
        ],
        series: [
          {
            name: "值",
            type: "bar",
            zlevel: 1,
            barGap:"10%",
            itemStyle: {
              normal: {
                barBorderRadius: 30,
                color: new echarts.graphic.LinearGradient(0, 0, 1, 0, [
                  {
                    offset: 0,
                    color: "rgb(57,89,255,1)",
                  },
                  {
                    offset: 1,
                    color: "rgb(46,200,207,1)",
                  },
                ]),
              },
            },
            barWidth: 10,
            data: dataX,
          },
          {
            name: "背景",
            type: "bar",
            barWidth: 10,
            barGap: "-100%",
            data: salvProMax,
            itemStyle: {
              normal: {
                color: "rgba(24,31,68,.4)",
                barBorderRadius: 30,
              },
            },
          },
        ],
      });
      
    },
    initChart(){
      this.barChart = echarts.init(this.$refs.myChart,true);
      this.setOptions(this.barData)
    }
  },
  
  
};
</script>
